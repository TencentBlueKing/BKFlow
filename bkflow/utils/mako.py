from bamboo_engine.template.template import Template


def parse_mako_expression(expression, context):
    parsed_result = Template(expression).render(context)
    if parsed_result not in ["True", "False"]:
        raise ValueError(f"The result of the expression must be a boolean, now is {parsed_result}")
    return parsed_result == "True"
